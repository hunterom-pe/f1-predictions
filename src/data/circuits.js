export const getCircuitMetadata = (circuitId) => {
  // Normalize circuit name/id
  const cleanId = String(circuitId || '').toLowerCase()
    .replace(/[^a-z0-9]/g, '_')
    .replace(/circuit/g, '')
    .trim();

  // Find partial key matches (e.g. "monza" in "autodromo_nazionale_monza")
  const key = Object.keys(circuitsData).find(k => cleanId.includes(k) || k.includes(cleanId)) || "monza";
  return circuitsData[key];
};
